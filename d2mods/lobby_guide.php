<h2>System Requirements</h2>
<p>The current release of the Alpha tools are only compatible with the following system configurations. You also need
    the following requirements to play Custom Games in Dota 2 currently.</p>
<ul>
    <li>Windows (preferably 7 or later)</li>
    <li>Direct3D 11 / Direct3D 9 compatible GPU</li>
    <li>At least 4gb of RAM</li>
    <li>Internet connection (preferably 1.5mbit/0.5mbit up and down... or greater)</li>
</ul>

<h2>Installing Dota 2 Workshop Tools</h2>
<p>In order to run Custom Games, you need to have the Dota 2 Workshop Tools installed. To install these tools, follow
    the steps given below.</p>
<p><strong>1.</strong> Go to Library and right click on Dota 2 and go to the Properties tab. Once you are in the
    Properties tab, go to the last tab called 'DLC' and tick the 'Dota 2 Workshop Tools DLC'. This will initiate a
    download of approximately 6.6GB.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/1-1.png" width="500px"/></div>
<p><strong>2.</strong> Go to the TOOLS tab from your Library menu.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/1-2a.png" width="500px"/></div>
<p>Once there, scroll down and find the 'Dota 2 Workshop Tools Alpha' and Install it.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/1-2b.png" width="500px"/></div>
<p><strong>3.</strong> Wait for both the Tools and DLC from the above steps to finish downloading.</p>

<h2>Subscribing to Custom Games</h2>
<p><strong>1.</strong> You can find all the list of uploaded Custom Games on the
    <a target="_blank" href="http://steamcommunity.com/workshop/browse/?appid=570&section=readytouseitems">Steam
        Workshop</a>
    at given point of time. Just visit the Workshop and click on the first category tab that reads
    '<a target="_blank" href="http://steamcommunity.com/workshop/browse/?appid=570&section=readytouseitems">Custom
        Games</a>'.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/2-1.png" width="500px"/></div>
<p><strong>2.</strong> Click the SUBSCRIBE button to start the automatic download via Steam. You can return at any point
    in the future to
    find other Custom Games to install, but only the Custom Games affiliated with us can be played through our Lobby
    Finder. This is mostly due to technical restrictions in the way lobbies work, but it's also partly because there are
    a lot of low effort Custom Games out there. If you want a map on here, point its developer here. We are always happy
    to welcome new Custom Games onto the site!</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/2-2.png" width="500px"/></div>

<h2>Install the GetDotaStats Lobby Explorer</h2>
<p><strong>1.</strong> You will need to download our
    <a target="_blank" href="https://github.com/GetDotaStats/GetDotaLobby/raw/master/play_weekend_tourney.swf">Lobby
        Finder via Github</a>.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/3-1.png" width="500px"/></div>
<p><strong>2.</strong> While this downloads, now is a good time to login in to our site via steam's OpenID. Click on the
    big login button at
    the top of this page. Signing in will ensure that you obtain full functionality of our Lobby Explorer. Not logging in
    may exclude you from some functionality.</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/3-2.png" width="500px"/></div>
<p><strong>3.</strong> Extract the play_weekend_tourney.swf that sits in the root directory of this zip into your Dota 2
    flash directory.
    This is normally located somewhere like C:\Program Files (x86)\Steam\SteamApps\common\dota 2
    beta\dota\resource\flash3</p>
<div class="text-center"><img src="//dota2.photography/images/lobbies/guide/3-3.png" width="500px"/></div>

<h2>Find/Join Lobbies</h2>
<p>Now that you have installed the Dota 2 Workshop Tools, installed the Lobby Explorer and logged into the site; it is now
    time for you to make use of our system!</p>
<p><strong>1.</strong> Start your Dota 2 client.</p>

<p><strong>2.</strong> Navigate to the PLAY CUSTOM GAMES tab and either create a lobby or join one via the list.</p>

