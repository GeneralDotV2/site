<p>GetDotaStats is proud to be part of the community. As part of giving back to the community, we host game servers in an attempt to support the fledgling custom maps community.</p>

<h3>Current Servers</h3>
<h4><a href="http://d2modd.in">D2Moddin</a></h4>
<p>We currently host servers in North America for D2Moddin. We also created and host the queue for entering the closed Beta (no longer required). At one point we also provided Australian servers, but have since had to withdraw that offering.</p>
<p>Support is provided via the Official <a target="_blank" href="http://forum.d2modd.in/">Forums</a>.</p>

