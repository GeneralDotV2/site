<h2>About</h2>
<p>The latest feature in the DBE Chrome Extension places each user's Dota 2 signature below their posts on the Dotabuff
    forums.</p>
<p>Feel free to leave comments in the chatbox on the side, or ping me on Reddit (u/jimmydorry). If users support this
    improvement, we will consider adding it to Firefox too!</p>
<p>You can find the page to re-install <a
        href="https://chrome.google.com/webstore/detail/dotabuff-extended/oggpckdeaofelnblpijaiijmgfckncdf?hl=en&gl=AU">HERE</a>.
    Read more about the extension <a href="http://getdotastats.com/dbe">HERE</a>.</p>
<p><strong>In a week or two, a decision will be made to keep or remove the signatures... so every vote counts!</strong>
